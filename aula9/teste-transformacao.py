#transformação
frase = 'Curso em Video Python'
print(frase.replace('Python', 'Android')) #mudou a palavra Python para Android, substitui de forma secundaria
print(frase.upper()) #deixa toda a string em maisculo-metodo
print(frase.lower()) #deixa toda a string em minusculo-metodo
print(frase.capitalize()) #A primeira letra fica maiscula enquanto todas as outras continuam minuscula
print(frase.title()) #mostra todas as primeira letras da frase em maisculo e o resto em minusculo,onde tiver espaço tera uma quebra de palavra