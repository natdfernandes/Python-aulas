#fatiamento
frase = 'Curso em Video Python'
print(frase[9]) #começo do indice
print(frase[9:13]) #começa do 9 até o 12 exclui o 13, sempre 1 a menos no final
print(frase[9:21]) #Fatiamento em Python
print(frase[9:21:2]) #vai pulando de dois em dois
print(frase[:5]) #antes dos : é onde vai começar, depois dos : é onde vai terminar, seria o mesmo que começar com zero
print(frase[15:]) #15 até o final
print(frase[9::3]) #Começa do 9 e vai pulando de 3 em 3