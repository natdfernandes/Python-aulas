#transformação
frase = '   Aprenda Python  ' #3 espaços vazios no começo e 2 no final
print(frase.strip()) #remove todos os espaços do começo e do final da string
print(frase.rstrip()) #right-direita começa o espaço, remove o espaço da direita
print(frase.lstrip()) #left-esquerda, remove os espaços da esquerda e mantem o da direita