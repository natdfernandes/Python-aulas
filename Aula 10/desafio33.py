#Faça um programa que leia três números e mostre qual é o maior e qual é o menor número 

numero1 = int(input('Digite o primeiro número: '))
numero2 = int(input('Digite o segundo número: '))
numero3 = int(input('Digite o terceiro número: '))
numero = [numero1, numero2, numero3]

if numero>=numero:
    print('O número maior é {}'.format(numero>numero))
    print('O segundo número maior é {}'.format(numero))
else:
    print('O número menor é {}'.format(numero < numero))
